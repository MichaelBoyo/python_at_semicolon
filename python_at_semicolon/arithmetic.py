number_1 = int(input("enter number1: "))
number_2 = int(input("enter number2: "))
square_1 = number_1 ** 2
square_2 = number_2 ** 2
sum_of_squares = square_1 + square_2
diff_of_squares = square_1 - square_2
print("square of num1 is %d\n square of num2 is %d\n sum of both squares is %d\n difference between both squares is "
      "%d " % (square_1, square_2, sum_of_squares, diff_of_squares))
